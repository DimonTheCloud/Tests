import pytest

from midterm_A import separuj


@pytest.mark.parametrize(
    ("souradnice", "separace"),
    [
        ([(0.2, 0.3, 0.2), (0, 1.0, 1), (-0.5, 1.112, 0.85)], [0.2, 0, -0.5]),
        ([(0.563, 0.917, 0.909), (0.585, 0.912, 0.909), (0.574, 0.919, 0.911)], [0.563, 0.585, 0.574]),
        ([(0.563, 0.911, 0.908), (0.555, 0.915, 0.906), (0.572, 0.916, 0.908)], [0.563, 0.555, 0.572]),
        ([(0.553, 0.924, 0.905), (0.569, 0.908, 0.911), (0.572, 0.916, 0.907)], [0.553, 0.569, 0.572]),
    ]
)

def test_assignment(souradnice, separace):
    assert separuj(souradnice) == separace
