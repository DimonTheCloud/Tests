import pytest

from midterm_B import cell_growth_monitoring


@pytest.mark.parametrize(
    ("cell_sizes", "average_growth_rates"),
    [
        (
            [[10, 15, 20, 25, 30], [12, 18, 24, 30, 36], [8, 14, 26, 32, 40], [11, 15.5, 20, 24.5, 29],
             [3, 6, 9.5, 12, 16], [1.5, 3.5, 5.5, 7.5, 9.5]],
            [5.0, 6.0, 4.5, 2.0]),
        (
            [[2, 4, 6, 8, 10], [24, 36, 48, 60, 72], [16, 32, 48, 64, 80], [22, 31, 40, 49, 58], [6, 12, 18, 24, 30]],
            [2.0, 12.0, 16.0, 9.0, 6.0]
        ),
        (
            [[2.5, 4.5, 6, 8.5, 10], [15, 30, 45, 60, 75], [1, 3, 4, 6, 20], [22, 31, 40, 49, 58]],
            [15.0, 9.0]
        ),
        (
            [[6, 10, 14, 16.5, 20], [6, 8.5, 11, 13.5, 16], [1, 5, 9, 13, 17], [25, 30, 35, 40, 45],
             [0, 2, 4, 6, 8, 10]],
            [2.5, 4.0, 5.0, 2.0]),
        (
            [[3, 6, 9, 12, 15], [4, 5.5, 7.5, 9, 12]],
            [3.0]
        ),
    ]
)
def test_cell_growth_monitoring(cell_sizes, average_growth_rates):
    assert cell_growth_monitoring(cell_sizes) == average_growth_rates
